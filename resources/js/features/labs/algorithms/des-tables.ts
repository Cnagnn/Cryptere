/**
 * Apply a permutation table to a bit array
 *
 * @param bits - Input bit array
 * @param table - Permutation table (1-indexed positions)
 * @returns Permuted bit array
 */
export function permute(bits: number[], table: readonly number[]): number[] {
    return table.map((pos) => bits[pos - 1]); // Table is 1-indexed
}

/**
 * Apply S-box substitution to 6 bits
 *
 * @param sixBits - 6-bit input
 * @param sbox - 4x16 S-box
 * @returns 4-bit output
 */
export function sboxSubstitute(sixBits: number[], sbox: ReadonlyArray<readonly number[]>): number[] {
    // Row: first and last bits (b0, b5) → 0-3
    const row = (sixBits[0] << 1) | sixBits[5];
    // Column: middle 4 bits (b1-b4) → 0-15
    const col = (sixBits[1] << 3) | (sixBits[2] << 2) | (sixBits[3] << 1) | sixBits[4];

    const value = sbox[row][col];

    // Convert to 4-bit binary
    return [
        (value >> 3) & 1,
        (value >> 2) & 1,
        (value >> 1) & 1,
        value & 1,
    ];
}
