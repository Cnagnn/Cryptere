/**
 * GF(2^8) multiplication by 2 (xtime)
 * Left shift by 1, with XOR of 0x1b if overflow (AES irreducible polynomial)
 *
 * @param b - byte value (0-255)
 * @returns b * 2 in GF(2^8)
 */
export function xtime(b: number): number {
    const result = b << 1;
    if (result & 0x100) {
        // If overflow (bit 8 set), XOR with reduction polynomial 0x1b
        return (result & 0xff) ^ 0x1b;
    }
    return result & 0xff;
}

/**
 * GF(2^8) multiplication using peasant's algorithm
 *
 * @param a - first operand (0-255)
 * @param b - second operand (0-255)
 * @returns a * b in GF(2^8)
 */
export function gfMul(a: number, b: number): number {
    let result = 0;
    let aa = a;
    let bb = b;

    while (bb > 0) {
        // If low bit of b is set, add a to result
        if (bb & 1) {
            result ^= aa;
        }

        // Check if high bit of a is set before left shift
        const highBit = aa & 0x80;
        aa = xtime(aa);
        bb = bb >> 1;

        // If high bit was set, XOR with 0x1b after shift
        // (handled implicitly by xtime when overflow occurs)
    }

    return result & 0xff;
}

/**
 * SubWord: Apply S-Box to each byte of a 4-byte word
 * Used in key expansion
 */
export function subWord(word: number[]): number[] {
    return word.map((byte) => S_BOX[byte]);
}

/**
 * RotWord: Rotate 4-byte word left by 1 byte
 * Used in key expansion
 */
export function rotWord(word: number[]): number[] {
    return [word[1], word[2], word[3], word[0]];
}
